    private void doiMatKhau() {
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;" + "databaseName=quanli;user=sa;password=123";
            conn = DriverManager.getConnection(url);

            String sql_select = "select username from taikhoan where passwords = ?";
            PreparedStatement prsttm = conn.prepareStatement(sql_select);
            prsttm.setString(1, txt_MatKhauCu.getText());
            rs = prsttm.executeQuery();
            while (rs.next()) {
                String user = rs.getString(1);
                String sql_update = "update taikhoan set passwords=? where username = ? ";
                pst = conn.prepareStatement(sql_update);

                pst.setString(1, txt_MatKhauMoi.getText());
                pst.setString(2, user);
                pst.executeUpdate();

            }

            JOptionPane.showMessageDialog(null, "Đổi mật khẩu thành công");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
