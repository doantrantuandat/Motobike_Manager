    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        datLai();
        if (txt_MatKhauCu.getText().trim().isEmpty() || txt_MatKhauMoi.getText().trim().isEmpty()
                || txt_NhapLaiMatKhau.getText().trim().isEmpty()) {

            if (txt_MatKhauCu.getText().trim().isEmpty()) {
                lbl_MatKhauCu.setText("Bạn chưa điền mục này");
            }
            if (txt_MatKhauMoi.getText().trim().isEmpty()) {
                lbl_MatKhauMoi.setText("Bạn chưa điền mục này");
            }
            if (txt_NhapLaiMatKhau.getText().trim().isEmpty()) {
                lbl_NhapLaiMatKhau.setText("Bạn chưa điền mục này");
            }

        } else {

            datLai();
            String matKhauMoi = txt_MatKhauMoi.getText();
            String nhapLaiMatKhau = txt_NhapLaiMatKhau.getText();

            try {

                Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
                String url = "jdbc:sqlserver://localhost:1433;" + "databaseName=quanli;user=sa;password=123";

                conn = DriverManager.getConnection(url);

                String sql = "select * from taikhoan where passwords=?";
                pst = conn.prepareStatement(sql);

                pst.setString(1, txt_MatKhauCu.getText());
                rs = pst.executeQuery();
                if (rs.next()) {
                    if (!matKhauMoi.equalsIgnoreCase(nhapLaiMatKhau)) {
                        lbl_NhapLaiMatKhau.setText("Nhập lại chưa đúng");
                    } else {
                        int hoiNhac = JOptionPane.showConfirmDialog(this, "Bạn có muốn thay đổi",
                                "Chú Ý", JOptionPane.YES_NO_OPTION);
                        if (hoiNhac == JOptionPane.YES_OPTION) {
                            doiMatKhau();
                        }
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "Nhập sai mật khẩu cũ!");
                    txt_MatKhauCu.setText("");
                    txt_MatKhauMoi.setText("");
                    txt_NhapLaiMatKhau.setText("");
                }

            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Nhập sai định dạng!");
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

